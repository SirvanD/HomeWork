// Write a while loop that will iterate from 0 to 10. For each iteration of the for loop, it will multiply the number by 9 and log the result (e.g. "2 * 9 = 18").

var iteration = 1;

while (iteration <=10){
    var result = iteration *9;
    console.log(`${iteration} * 9 = ${result}`);
    iteration++
}

